# frozen_string_literal: true

require "spec_helper"

describe TimexDatalinkClient::Protocol9::Alarm do
  let(:number) { 1 }
  let(:audible) { false }
  let(:time) { Time.new(1994) }
  let(:message) { "Alarm 1" }
  let(:month) { nil }
  let(:day) { nil }

  let(:alarm) do
    described_class.new(
      number: number,
      audible: audible,
      time: time,
      message: message,
      month: month,
      day: day
    )
  end

  describe "#packets", :crc do
    subject(:packets) { alarm.packets }

    it_behaves_like "CRC-wrapped packets", [
      [
        0x50, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
        0x24, 0x24, 0x24, 0x24, 0x24
      ]
    ]

    context "when number is 2" do
      let(:number) { 2 }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end

    context "when audible is true" do
      let(:audible) { true }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x00, 0x00, 0x01, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end

    context "when time is 16:35" do
      let(:time) { Time.new(1994, 1, 1, 16, 35) }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x10, 0x23, 0x00, 0x00, 0x00, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end

    context "when message is \"Wake Up with More than 16 Characters\"" do
      let(:message) { "Wake Up with More than 8 Characters" }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x20, 0x0a, 0x14, 0x0e, 0x24, 0x1e, 0x19, 0x24, 0x20, 0x12, 0x1d,
          0x11, 0x24, 0x16, 0x18, 0x1b
        ]
      ]
    end

    context "when message is \";@_|<>[]" do
      let(:message) { ";@_|<>[]" }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x36, 0x38, 0x3a, 0x3b, 0x3c, 0x3d, 0x3e, 0x3f, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end

    context "when message is \"~with~invalid~characters\"" do
      let(:message) { "~with~invalid~characters" }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x24, 0x20, 0x12, 0x1d, 0x11, 0x24, 0x12, 0x17, 0x1f, 0x0a, 0x15,
          0x12, 0x0d, 0x24, 0x0c, 0x11
        ]
      ]
    end

    context "when month is 12" do
      let(:month) { 12 }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x0c, 0x00, 0x00, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end

    context "when day is 25" do
      let(:day) { 25 }

      it_behaves_like "CRC-wrapped packets", [
        [
          0x50, 0x01, 0x00, 0x00, 0x00, 0x19, 0x00, 0x0a, 0x15, 0x0a, 0x1b, 0x16, 0x24, 0x01, 0x24, 0x24, 0x24, 0x24,
          0x24, 0x24, 0x24, 0x24, 0x24
        ]
      ]
    end
  end
end
